  function getLang() { return _lang; }

  function setLang(l) {
    if (!LANGS.includes(l)) return _lang;
    _lang = l;
    try { if (typeof localStorage !== 'undefined') localStorage.setItem(STORAGE_KEY, l); } catch (_) {}
    return _lang;
  }

  // t(key, vars?) — stringa nella lingua corrente.
  // Fallback: lingua corrente → DEFAULT_LANG → la chiave stessa (così una
  // chiave non tradotta è visibile, non rompe la UI). Interpolazione: {nome}.
  function t(key, vars) {
    let s = DICT[_lang] && DICT[_lang][key];
    if (s == null) s = DICT[DEFAULT_LANG] && DICT[DEFAULT_LANG][key];
    if (s == null) s = key;
    if (vars) s = String(s).replace(/\{(\w+)\}/g, (m, k) => (vars[k] != null ? vars[k] : m));
    return s;
  }

  // isGlossaryTerm(term) — true se è un termine tecnico da non tradurre.
  function isGlossaryTerm(term) {
    const x = String(term).trim().toLowerCase();
    return GLOSSARY.some(g => g.toLowerCase() === x);
  }

